public class DataType {
	public static void main(String[] args) {
		System.out.println("1 / 2 = " + 1/2 + ", int type ");
		System.out.println("1 % 2 = " + 1%2 + ", int type ");
		System.out.println("1.0 / 2 = " + 1.0/2 + ", double type ");
		System.out.println("5 + 7 / 2 = " + (5 + 7 / 2) + ", int type ");
		System.out.println("\"Beat\" + " + "' '" + " + \"Army\" = " + "Beat Army" + ", String type "  );
		System.out.println("6 + 13 / 5 - 35 % 3 = " +(6 + 13 / 5 - 35 % 3)+ ", int type " );
		System.out.println("3.5 * (5/4) = " + (3.5 * (5/4)) + ", double type");
		System.out.println("(3.5 * 5)/4 = " + ((3.5 * 5)/4) + ", double type");
	}
}
